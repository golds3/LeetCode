// @lc code=start
class Solution {
    public int minDistance(String word1, String word2) {
        int n = word1.length();
        int m = word2.length();
        int[][] dp = new int[n+1][m+1];
        //base case
        for(int i = 0;i<=n;i++){
            dp[i][0] = i;
        }
        for(int j = 0;j<=m;j++){
            dp[0][j] = j;
        }
        for(int i = 1;i<=n;i++){
            for(int j = 1;j<=m;j++){
                if(word1.charAt(i-1)==word2.charAt(j-1)){
                    dp[i][j] = dp[i-1][j-1];
                }else{
                    dp[i][j] = Math.min(Math.min(dp[i-1][j]+1,dp[i][j-1]+1),dp[i-1][j-1]+1);
                    // //插入  word2添加一个元素，相当于word1删除一个元素
                    // dp[i][j-1]+1
                    // //删除
                    // dp[i-1][j]+1
                    // //修改 
                    // dp[i-1][j-1]+1
                }
            }
        }
        return dp[n][m];


    }
}
